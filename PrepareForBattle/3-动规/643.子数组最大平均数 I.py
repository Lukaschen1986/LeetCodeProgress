def findMaxAverage(nums, k) -> float:
    """
    time = O(N)
    space = O(1)
    """
    if k == 1:
        return max(nums)
    n = len(nums)
    res = sum(nums[0:k]) # 神来之笔
    dp = res
    
    for i in range(k,n):
        dp = dp - nums[i-k] + nums[i]
        res = max(res, dp)
    
    return res/k


